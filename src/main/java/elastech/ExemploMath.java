package elastech;

public class ExemploMath {

    public static void main(String[] args) {
        System.out.println(Math.PI);

        // potencia
        System.out.println(Math.pow(2, 3));

        int a = 3;
        System.out.println(Math.pow(2, a));

        // raiz quadrada
        System.out.println(Math.sqrt(16));


    }
}
